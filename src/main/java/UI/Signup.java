    private void SignupsignupbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignupsignupbtnActionPerformed
        // TODO add your handling code here:
  String firstName = Signupfname.getText();
    String lastName = Signuplname.getText();
    String email = Signupemail.getText();
    String phone = Signupphone.getText();
    String address = Signupaddress.getText();
    String password = new String(SignupPassword.getPassword()); // Fetch password
    String confirmPassword = new String(Signupconfirmpassword.getPassword()); // Fetch confirm password

    // Validate input
    if (firstName.isEmpty() || lastName.isEmpty() || email.isEmpty() || phone.isEmpty() || address.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all the fields.");
    } else if (!password.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match.");
    } else {
        // Create a new Customer object and save it
        Customer customer = new Customer(firstName, lastName, email, phone, address, password);  // Include password
        CustomerController customerController = new CustomerController();
        boolean isSaved = customerController.saveCustomer(customer);
        
        if (isSaved) {
            JOptionPane.showMessageDialog(this, "Signup successful");
        } else {
            JOptionPane.showMessageDialog(this, "Signup failed");
        }
    }
    }//GEN-LAST:event_SignupsignupbtnActionPerformed

  
